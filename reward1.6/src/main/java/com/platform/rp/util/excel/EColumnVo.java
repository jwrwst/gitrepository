package com.platform.rp.util.excel;

import com.platform.rp.util.excel.EColumn.EColumnType;

public class EColumnVo {

	// 字段名称
	private String name;
	// 列索引
	private int index;
	// 列名
	private String title;
	// 整列的格式
	private EColumnType type;

	private double width;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getIndex() {
		return index;
	}

	public void setIndex(int index) {
		this.index = index;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public EColumnType getType() {
		return type;
	}

	public void setType(EColumnType type) {
		this.type = type;
	}

	public double getWidth() {
		return width;
	}

	public void setWidth(double width) {
		this.width = width;
	}

	@Override
	public String toString() {
		return "EColumnVo [name=" + name + ", index=" + index + ", title=" + title + ", type=" + type + ", width="
				+ width + "]";
	}

}
