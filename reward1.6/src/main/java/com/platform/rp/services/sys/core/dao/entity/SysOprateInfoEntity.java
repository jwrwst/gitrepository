package com.platform.rp.services.sys.core.dao.entity;

import java.sql.Timestamp;

public class SysOprateInfoEntity {

	private int id;
	private String module;// 操作入口。类+方法
	private String operateLog;// 操作日志信息
	private int type;
	private String operator; // 操作人

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getModule() {
		return module;
	}

	public void setModule(String module) {
		this.module = module;
	}

	public String getOperateLog() {
		return operateLog;
	}

	public void setOperateLog(String operateLog) {
		this.operateLog = operateLog;
	}

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}

	public String getOperator() {
		return operator;
	}

	public void setOperator(String operator) {
		this.operator = operator;
	}

	@Override
	public String toString() {
		return "SysOprateInfoEntity [id=" + id + ", module=" + module + ", operateLog=" + operateLog + ", type=" + type
				+ ", operator=" + operator + "]";
	}

}
