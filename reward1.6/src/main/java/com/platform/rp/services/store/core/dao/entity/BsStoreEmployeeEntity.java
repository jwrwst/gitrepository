package com.platform.rp.services.store.core.dao.entity;

import java.io.Serializable;
import java.util.Date;


/**
 * The persistent class for the bs_store_employee database table.
 * 
 */
public class BsStoreEmployeeEntity implements Serializable {
	private static final long serialVersionUID = 1L;
	private long id;
	private long empId;
	private String empName;
	private String empNickName;
	private int status;
	private long storeId;
	private Date updateTime;
	
	private String jobTitle;
	
	private String headPic;	

	public BsStoreEmployeeEntity() {
	}
	
	

	public String getJobTitle() {
		return jobTitle;
	}



	public void setJobTitle(String jobTitle) {
		this.jobTitle = jobTitle;
	}



	public String getHeadPic() {
		return headPic;
	}



	public void setHeadPic(String headPic) {
		this.headPic = headPic;
	}



	public String getEmpNickName() {
		return empNickName;
	}




	public void setEmpNickName(String empNickName) {
		this.empNickName = empNickName;
	}




	public String getEmpName() {
		return empName;
	}

	public void setEmpName(String empName) {
		this.empName = empName;
	}

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public long getEmpId() {
		return empId;
	}

	public void setEmpId(long empId) {
		this.empId = empId;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}

	public long getStoreId() {
		return storeId;
	}

	public void setStoreId(long storeId) {
		this.storeId = storeId;
	}


	public Date getUpdateTime() {
		return updateTime;
	}

	public void setUpdateTime(Date updateTime) {
		this.updateTime = updateTime;
	}


	
}